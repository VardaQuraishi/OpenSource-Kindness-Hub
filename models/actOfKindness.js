const mongoose = require('mongoose');

// Define the schema for the Act of Kindness model
const actOfKindnessSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  image: {
    type: String,
    required: true
  },
  tags: {
    type: [String],
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

// Create and export the ActOfKindness model based on the schema
module.exports = mongoose.model('ActOfKindness', actOfKindnessSchema);
